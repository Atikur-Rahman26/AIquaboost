class UserInfoData {
  String userID;
  String full_name;
  String email;
  String phone_number;
  String preference;
  String role;
  String age;
  String user_profile_photo;

  UserInfoData({
    required this.userID,
    required this.full_name,
    required this.email,
    required this.phone_number,
    required this.preference,
    required this.role,
    required this.age,
    required this.user_profile_photo,
  });
}
